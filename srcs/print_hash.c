#include "ft_ssl_md5.h"

void	print_hash(uint32_t *digest, uint64_t size)
{
	uint32_t	i;

	i = 0;
	while (i < size / 4)
	{
		printf("%8.8x", swap_int32(digest[i]));
		i++;
	}
}
